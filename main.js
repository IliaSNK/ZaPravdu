let renderJoining = () => {
  let selGroup = document.getElementById('group-select')
  let group = ''


  let navalny = ''
  let er = ''
  let ldpr = ''
  let kprf = ''


  if (selGroup == null) {
    group = 'navalny'
  } else {
    group = selGroup.value
    if (selGroup.value == "er") {
      er = 'selected'
    }
    if (selGroup.value == "navalny") {
      navalny = 'selected'
    }
    if (selGroup.value == "ldpr") {
      ldpr = 'selected'
    }
    if (selGroup.value == "kprf") {
      kprf = 'selected'
    }
  }
  root.innerHTML =
    `
    <strong>Диаграмма Венна пересечения аудиторий</strong>
    <img src="img/logo.svg" alt="" class="logo" onclick="renderMain()">
    <select name="" id="group-select" class="select-group" onchange="renderJoining()">
        <option ${navalny} value="navalny">Навальный</option>
        <option ${er} value="er">Единая Россия</option>
        <option ${ldpr} value="ldpr">ЛДПР</option>
        <option ${kprf} value="kprf">КПРФ</option>
    </select>
    `
  arr.forEach(el => {
    root.innerHTML +=

      `
        <div class="group">
            <a href="${el.href}" target="_blank">${el.title}</a>
            <div class="diagrams-img">
                <div>
                    <img src="${el[group]}" alt="" style="width: 600px">
                </div>
               
            </div>
        </div>
        `
  })
}

